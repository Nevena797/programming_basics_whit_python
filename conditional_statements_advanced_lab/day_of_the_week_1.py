number = int(input())
message_for_print = ""
if number == 1:
    message_for_print = "Monday"
elif number == 2:
    message_for_print = "Tuesday"
elif number == 3:
    message_for_print = "Wednesday"
elif number == 4:
    message_for_print = "Thursday"
elif number == 5:
    message_for_print = "Friday"
elif number == 6:
    message_for_print = "Saturday"
elif number == 7:
    message_for_print = "Sunday"
else:
    message_for_print = "Error"
print(message_for_print)
